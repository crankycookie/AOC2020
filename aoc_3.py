def convertLinesToList(lines):
  return lines.strip('\n').split('\n')


def testLine(idx, line):
  wrappedIdx = idx%len(line)
  if line[wrappedIdx] == '#':
    return True
  else:
    return False

def countTreesInPath(right, down, lines):
  treeCounter = 0
  x = 0
  y = 0

  while y+1 < len(lines):
    x = x + right
    y = y + down
    line = lines[y]
    if testLine(x, line):
      treeCounter = treeCounter + 1
  
  return treeCounter

